    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        // TODO add your handling code here:
        try {
            int id=Integer.parseInt(spnIdJugador.getValue().toString());
        String nombre=txtNombre.getText();
        int edad=Integer.parseInt(spnEdad.getValue().toString());
        String nacionalidad=cboNacionalidad.getSelectedItem().toString();
        int valorus=Integer.parseInt(txtValor.getText());
        String posicion=cboPosicion.getSelectedItem().toString();
        int id_posicion=0;
        for ( Posicion item : new DaoPosicion().listar()) {
            if (item.getDescripcion().equalsIgnoreCase(posicion)) {
                id_posicion=item.getIdPosicion();
            }
        }
        DaoJugador dao=new DaoJugador();
        Jugador juga=new Jugador(id, nombre, edad, nacionalidad, id_posicion, valorus);
        boolean resp=dao.agregarJugador(juga);
        if (resp==true) {
            JOptionPane.showMessageDialog(null, "Grabo Jugador");
        } else {
            JOptionPane.showMessageDialog(null, "No Grabo Jugador");
        }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btngrabarActionPerformed

