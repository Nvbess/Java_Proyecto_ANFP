    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        // TODO add your handling code here:
        try {
            int id=Integer.parseInt(snpIdentificadorClub.getValue().toString());
            String Nombre=txtNombreClub.getText();
            Date fecha_f=DateFechaFundacion.getDate();
            LocalDate fecha_real= fecha_f.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            String presidente=txtPresidente.getText();
            String estadio=txtEstadio.getText();
            int id_ent=0;
            String nombre_ent=cboEntrenador.getSelectedItem().toString();
            for (Entrenador item : new DaoEntenador().listarEntrenador()) {
                if (item.getNombre().equalsIgnoreCase(nombre_ent)) {
                    id_ent=item.getIdEntrenador();
                }
            }
            DaoClub dao=new DaoClub();
            Club clu=new Club(id, Nombre, fecha_real, presidente, estadio, id_ent);
            boolean resp= dao.agregarClub(clu);
            if (resp==true) {
                JOptionPane.showMessageDialog(null, "Grabo");
            } else {
                JOptionPane.showMessageDialog(null, "No Grabo");
            }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btnGrabarActionPerformed

