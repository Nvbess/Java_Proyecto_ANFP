    private void LlenarcomboEntrenador() {
        ArrayList<Entrenador> lista=new DaoEntenador().listarEntrenador();
        cboEntrenador.removeAllItems();
        cboEntrenador.addItem("Seleccione...");
        for (Entrenador item : lista) {
            cboEntrenador.addItem(item.getNombre());
        }
    }
